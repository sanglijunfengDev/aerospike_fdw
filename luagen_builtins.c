char *codegen_expr(LuagenContext *context, Expr *node,Oid foreignTableId)
{
	switch (nodeTag(node))
	{
		case T_Var:
        {
            char *ret;
            Var *var = (Var *) node;
    	    char  *column_name;
            Relation rd;
            rd = RelationIdGetRelation(foreignTableId);
    	    column_name = RIAttName(rd, var->varattno);
            RelationClose(rd);
            int len;
            len = strlen(column_name);
            ret = palloc0(len + 4);
            sprintf(ret, "rec.%s", column_name);
            return ret;
	    }
        case T_Const:
        {
            int lenth;
            char buf[64];
            char *ret = NULL;
            Const *con = (Const *)node;
	        char *val;
            if (con->consttype == 20 ||
                con->consttype == 21 ||
                con->consttype == 23)
            {
                sprintf(buf, "%ld", con->constvalue);
                lenth = strlen(buf);
                ret = palloc0(lenth + 1);
                memcpy(ret, buf, lenth);
                return ret;
            }
            else
            {
                Oid             typoutputfunc;
                bool            typIsVarlena;
                getTypeOutputInfo(con->consttype, &typoutputfunc, &typIsVarlena);
                val  =  DatumGetCString(OidFunctionCall1(typoutputfunc,con->constvalue));
                return val;
            }
            break;
        }
        case T_OpExpr:
        {
            OpExpr *op = (OpExpr*) node;
            const LuagenBuiltin *f = luagen_isbuiltin(op->opfuncid);
            if (f->func != NULL)
            {
                return f->func(context, (Expr*)op,foreignTableId);
            }
            else
            {
                elog(LOG, "Temporarily does not support\n");
                return NULL;
            }
            break;
        }
        case T_FuncExpr:
        {
        	FuncExpr   *func = (FuncExpr *) node;
        	const LuagenBuiltin *f = luagen_isbuiltin(func->funcid);
            if (func->funcid == 1299)
            {
                char *now = "os.time()";
                return now;
            }

            if (func->args != NULL)
            {
        	    return codegen_expr(context,(Expr *)(linitial(func->args)),foreignTableId);
            }
	    }
			break;
		case T_CoerceViaIO:
		{
			char *ret;
			char *str_tmp;
			CoerceViaIO *iocoerce = (CoerceViaIO *) node;
			str_tmp = codegen_expr(context,(Expr *)iocoerce->arg,foreignTableId);
			if (iocoerce->resulttype == 20)
			{
				ret = palloc0(strlen(str_tmp) + strlen("tonumber()") + 1);
				sprintf(ret, "tonumber(%s)", str_tmp);
			}
			return ret;
		}
			break;
        default:
			return NULL;
			break;
    }
}

